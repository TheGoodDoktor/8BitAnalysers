// =====================================================================================
// Tier-A C++ recompiler backend (SKELETON). See CppExporter.h header comment.
//
// Control-flow model: one big translation function per program, basic blocks emitted as
// goto-labelled sections in address order. This handles overlapping / fall-through entry
// points (e.g. AddAToHL* at $AEE3/4/5) for free, because every entry is just a label.
// =====================================================================================

// -------------------------------------------------------------------------------------
// Naming helpers
// -------------------------------------------------------------------------------------

std::string FCppExporter::BlockLabel(FAddressRef addr) const
{
	char buf[32];
	snprintf(buf, sizeof(buf), "L_%04X", addr.Address);
	return buf;
}

std::string FCppExporter::FunctionName(FAddressRef addr) const
{
	FCodeAnalysisState& state = pEmulator->GetCodeAnalysis();
	const FLabelInfo* pLabel = state.GetLabelForAddress(addr);
	if (pLabel != nullptr && pLabel->GetName()[0] != '\0')
		return pLabel->GetName();

	char buf[32];
	snprintf(buf, sizeof(buf), "func_%04X", addr.Address);
	return buf;
}

std::string FCppExporter::DisassemblyText(FAddressRef addr)
{
	FCodeAnalysisState& state = pEmulator->GetCodeAnalysis();
	const FCodeInfo* pCodeInfo = state.GetCodeInfoForAddress(addr);
	if (pCodeInfo != nullptr && pCodeInfo->Text.empty() == false)
		return pCodeInfo->Text;	// stored disassembly; good enough for a provenance comment
	return "<no disassembly>";
}

// -------------------------------------------------------------------------------------
// Runtime preamble (header)
// -------------------------------------------------------------------------------------

void FCppExporter::EmitCpuStateStruct(void)
{
	const bool bC = RecompilerConfig.bEmitC;
	Output("// ---- CPU state ---------------------------------------------------------\n");
	Output(bC ? "typedef struct\n{\n" : "struct Z80CpuState\n{\n");
	Output("\tuint8_t  A, F, B, C, D, E, H, L;\n");
	Output("\tuint8_t  A_, F_, B_, C_, D_, E_, H_, L_;   // shadow registers\n");
	Output("\tuint16_t IX, IY, SP, PC;\n");
	Output("\tuint8_t  I, R;\n");
	Output("\tbool     IFF1, IFF2;\n");
	Output("\tuint8_t  IM;\n");
	Output("\tbool     Halted;\n");
	Output("\tuint64_t Cycles;          // for timing-sensitive code / IRQ gating\n");
	Output("\tuint8_t* Mem;             // 64K (or paging-window) byte array, AUTHORITATIVE\n");
	Output(bC ? "} Z80CpuState;\n\n" : "};\n\n");
}

void FCppExporter::EmitRuntimeDeclarations(void)
{
	const char* pCpu = CpuArgDecl();
	Output("// ---- Runtime hooks (provided by the harness) ---------------------------\n");
	Output("// Memory is the authoritative source of truth so that self-modifying code,\n");
	Output("// banking and I/O all remain interception points rather than special cases.\n");
	Output("uint8_t  Read8 (%s, uint16_t addr);\n", pCpu);
	Output("void     Write8(%s, uint16_t addr, uint8_t val);\n", pCpu);
	Output("uint8_t  In    (%s, uint16_t port);\n", pCpu);
	Output("void     Out   (%s, uint16_t port, uint8_t val);\n\n", pCpu);
	Output("// Control-flow helpers. Call/JumpTo/DispatchIndirect look up an address->handler\n");
	Output("// map; unresolved indirect targets (jump tables) route through DispatchIndirect.\n");
	Output("void     Call           (%s, uint16_t target);\n", pCpu);
	Output("void     JumpTo         (%s, uint16_t target);\n", pCpu);
	Output("void     DispatchIndirect(%s, uint16_t target);\n\n", pCpu);
}

void FCppExporter::EmitEntryPointDeclarations(void)
{
	Output("// ---- Entry points ------------------------------------------------------\n");
	for (const FAddressRef& entry : CFG.EntryPoints)
		Output("void %s(%s);   // 0x%04X\n", FunctionName(entry).c_str(), CpuArgDecl(), entry.Address);
	Output("\n");
}

void FCppExporter::AddHeader(void)
{
	Output("// Machine-generated by 8BitAnalysers FCppExporter (Tier A - faithful translation).\n");
	Output("// Target: %s. DO NOT EDIT: regenerate from the analysis project instead.\n\n",
		RecompilerConfig.bEmitC ? "C99" : "C++");
	if (RecompilerConfig.bEmitC)
		Output("#include <stdint.h>\n#include <stdbool.h>\n\n");
	else
		Output("#include <cstdint>\n\n");
	EmitCpuStateStruct();
	EmitRuntimeHelpers();
	EmitRuntimeDeclarations();
	// Entry-point declarations depend on the CFG, which is built in ExportProgram;
	// they are appended there once CFG.EntryPoints is populated.
}

// Inline Z80 flag/ALU primitives. These are transcribed from the chips Z80 core
// (Source/Vendor/chips/chips/z80.h) so generated code is bit-accurate against the
// emulator used for differential validation. Identical text in C and C++.
void FCppExporter::EmitRuntimeHelpers(void)
{
	Output("// ---- Z80 flag/ALU runtime (mirrors Vendor/chips/chips/z80.h) -----------\n");
	Output("enum { Z80_CF=1<<0, Z80_NF=1<<1, Z80_VF=1<<2, Z80_PF=Z80_VF,\n");
	Output("       Z80_XF=1<<3, Z80_HF=1<<4, Z80_YF=1<<5, Z80_ZF=1<<6, Z80_SF=1<<7 };\n\n");

	Output("static inline uint8_t z80_sz(uint8_t v){ return (v!=0)?(v&Z80_SF):Z80_ZF; }\n");
	Output("static inline uint8_t z80_szp(uint8_t v){\n");
	Output("\tuint8_t p=v; p^=p>>4; p^=p>>2; p^=p>>1;\n");
	Output("\treturn z80_sz(v) | (v&(Z80_YF|Z80_XF)) | ((p&1)?0:Z80_PF);\n}\n");
	Output("static inline uint8_t z80_szyxch(uint8_t a,uint8_t v,uint32_t r){\n");
	Output("\treturn z80_sz((uint8_t)r)|(r&(Z80_YF|Z80_XF))|((r>>8)&Z80_CF)|((a^v^r)&Z80_HF);\n}\n");
	Output("static inline uint8_t z80_addf(uint8_t a,uint8_t v,uint32_t r){\n");
	Output("\treturn z80_szyxch(a,v,r)|((((v^a^0x80)&(v^r))>>5)&Z80_VF);\n}\n");
	Output("static inline uint8_t z80_subf(uint8_t a,uint8_t v,uint32_t r){\n");
	Output("\treturn Z80_NF|z80_szyxch(a,v,r)|((((v^a)&(r^a))>>5)&Z80_VF);\n}\n");
	Output("static inline uint8_t z80_cpf(uint8_t a,uint8_t v,uint32_t r){\n");
	Output("\treturn Z80_NF|z80_sz((uint8_t)r)|(v&(Z80_YF|Z80_XF))|((r>>8)&Z80_CF)|((a^v^r)&Z80_HF)|((((v^a)&(r^a))>>5)&Z80_VF);\n}\n\n");

	Output("static inline void z80_add8(Z80CpuState* c,uint8_t v){ uint32_t r=c->A+v;            c->F=z80_addf(c->A,v,r); c->A=(uint8_t)r; }\n");
	Output("static inline void z80_adc8(Z80CpuState* c,uint8_t v){ uint32_t r=c->A+v+(c->F&Z80_CF); c->F=z80_addf(c->A,v,r); c->A=(uint8_t)r; }\n");
	Output("static inline void z80_sub8(Z80CpuState* c,uint8_t v){ uint32_t r=(uint32_t)((int)c->A-(int)v);              c->F=z80_subf(c->A,v,r); c->A=(uint8_t)r; }\n");
	Output("static inline void z80_sbc8(Z80CpuState* c,uint8_t v){ uint32_t r=(uint32_t)((int)c->A-(int)v-(c->F&Z80_CF));c->F=z80_subf(c->A,v,r); c->A=(uint8_t)r; }\n");
	Output("static inline void z80_and8(Z80CpuState* c,uint8_t v){ c->A&=v; c->F=z80_szp(c->A)|Z80_HF; }\n");
	Output("static inline void z80_xor8(Z80CpuState* c,uint8_t v){ c->A^=v; c->F=z80_szp(c->A); }\n");
	Output("static inline void z80_or8 (Z80CpuState* c,uint8_t v){ c->A|=v; c->F=z80_szp(c->A); }\n");
	Output("static inline void z80_cp8 (Z80CpuState* c,uint8_t v){ uint32_t r=(uint32_t)((int)c->A-(int)v); c->F=z80_cpf(c->A,v,r); }\n");
	Output("static inline uint8_t z80_inc8(Z80CpuState* c,uint8_t v){ uint8_t r=v+1; uint8_t f=z80_sz(r)|(r&(Z80_XF|Z80_YF))|((r^v)&Z80_HF); if(r==0x80)f|=Z80_VF; c->F=f|(c->F&Z80_CF); return r; }\n");
	Output("static inline uint8_t z80_dec8(Z80CpuState* c,uint8_t v){ uint8_t r=v-1; uint8_t f=Z80_NF|z80_sz(r)|(r&(Z80_XF|Z80_YF))|((r^v)&Z80_HF); if(r==0x7F)f|=Z80_VF; c->F=f|(c->F&Z80_CF); return r; }\n\n");
}

// -------------------------------------------------------------------------------------
// Instruction & block emission
// -------------------------------------------------------------------------------------

void FCppExporter::EmitInstruction(FAddressRef addr)
{
	FCodeAnalysisState& state = pEmulator->GetCodeAnalysis();
	const FInstructionFlow flow = ClassifyInstruction(state, addr);

	// Provenance comment - always emitted so the generated code stays auditable.
	Output("\t// %04X: %s\n", addr.Address, DisassemblyText(addr).c_str());

	// Calls happen mid-block and must be emitted in stream order.
	if (flow.bIsCall && flow.Target.IsValid())
	{
		if (flow.bConditional)
			Output("\tif (%s) Call(%s, 0x%04X); // %s\n",
				ConditionExpr(addr).c_str(), CpuPtr(), flow.Target.Address, FunctionName(flow.Target).c_str());
		else
			Output("\tCall(%s, 0x%04X); // %s\n", CpuPtr(), flow.Target.Address, FunctionName(flow.Target).c_str());
		return;
	}

	// Control-flow instructions (JP/JR/RET/DJNZ/HALT/JP(HL)) carry no inline data
	// semantics - their transfer is emitted by EmitTerminator. Note this is keyed off the
	// instruction's own classification, NOT "is it the block's last instruction": a block
	// that ends by fall-through (because the next address is a leader) still needs the
	// semantics of its final, ordinary instruction emitted.
	if (flow.bEndsBlock)
		return;

	if (state.CPUInterface->CPUType == ECPUType::Z80)
		EmitInstructionSemanticsZ80(addr);
	else
		Output("\t/* TODO(Phase 1): semantics (non-Z80 CPU) */\n");
}

// Phase 1 vertical slice: NOP, LD r,r', LD r,n, INC/DEC r, 8-bit ALU A,r and A,n.
// Decoded via the regular octal opcode structure (x/y/z fields, see z80.info/decoding.htm).
// Anything outside the slice falls back to a clearly-marked TODO so output still builds.
void FCppExporter::EmitInstructionSemanticsZ80(FAddressRef addr)
{
	FCodeAnalysisState& state = pEmulator->GetCodeAnalysis();
	const uint8_t op = state.ReadByte(addr);
	const int x = op >> 6, y = (op >> 3) & 7, z = op & 7;

	static const char* kRegName[8] = { "B", "C", "D", "E", "H", "L", "(HL)", "A" };
	static const char* kAlu[8]     = { "z80_add8", "z80_adc8", "z80_sub8", "z80_sbc8",
	                                    "z80_and8", "z80_xor8", "z80_or8",  "z80_cp8" };
	const char* acc = Acc();

	// Read expression for register/(HL) operand index r (0..7).
	auto regRead = [&](int r) -> std::string
	{
		if (r == 6)	// (HL)
		{
			char buf[64];
			snprintf(buf, sizeof(buf), "Read8(%s, (%sH<<8)|%sL)", CpuPtr(), acc, acc);
			return buf;
		}
		return std::string(acc) + kRegName[r];
	};

	if (op == 0x00) { Output("\t/* NOP */\n"); return; }

	if (x == 1)	// LD r[y], r[z]   (0x76 HALT is a terminator, never reaches here)
	{
		const std::string src = regRead(z);
		if (y == 6)	// LD (HL), r
			Output("\tWrite8(%s, (%sH<<8)|%sL, %s);\n", CpuPtr(), acc, acc, src.c_str());
		else
			Output("\t%s%s = %s;\n", acc, kRegName[y], src.c_str());
		return;
	}

	if (x == 2)	// ALU[y] A, r[z]
	{
		Output("\t%s(%s, %s);\n", kAlu[y], CpuPtr(), regRead(z).c_str());
		return;
	}

	if (x == 3 && z == 6)	// ALU[y] A, n   (0xC6/0xCE/0xD6/.../0xFE)
	{
		const uint8_t n = state.ReadByte(addr.Address + 1);
		Output("\t%s(%s, 0x%02X);\n", kAlu[y], CpuPtr(), n);
		return;
	}

	if (x == 0)
	{
		if (z == 4)	// INC r[y]
		{
			if (y == 6)
				Output("\tWrite8(%s, (%sH<<8)|%sL, z80_inc8(%s, Read8(%s, (%sH<<8)|%sL)));\n",
					CpuPtr(), acc, acc, CpuPtr(), CpuPtr(), acc, acc);
			else
				Output("\t%s%s = z80_inc8(%s, %s%s);\n", acc, kRegName[y], CpuPtr(), acc, kRegName[y]);
			return;
		}
		if (z == 5)	// DEC r[y]
		{
			if (y == 6)
				Output("\tWrite8(%s, (%sH<<8)|%sL, z80_dec8(%s, Read8(%s, (%sH<<8)|%sL)));\n",
					CpuPtr(), acc, acc, CpuPtr(), CpuPtr(), acc, acc);
			else
				Output("\t%s%s = z80_dec8(%s, %s%s);\n", acc, kRegName[y], CpuPtr(), acc, kRegName[y]);
			return;
		}
		if (z == 6)	// LD r[y], n
		{
			const uint8_t n = state.ReadByte(addr.Address + 1);
			if (y == 6)
				Output("\tWrite8(%s, (%sH<<8)|%sL, 0x%02X);\n", CpuPtr(), acc, acc, n);
			else
				Output("\t%s%s = 0x%02X;\n", acc, kRegName[y], n);
			return;
		}
	}

	// Outside the Phase 1 slice (16-bit ops, prefixes, I/O, stack, rotates, ...).
	Output("\t/* TODO(Phase 1): semantics for opcode 0x%02X */\n", op);
}

std::string FCppExporter::ConditionExpr(FAddressRef addr)
{
	FCodeAnalysisState& state = pEmulator->GetCodeAnalysis();
	const uint8_t op = state.ReadByte(addr);
	const char* acc = Acc();
	char buf[64];

	if (op == 0x10)	// DJNZ: decrement B and branch if non-zero (does not touch flags)
	{
		snprintf(buf, sizeof(buf), "(--%sB != 0)", acc);
		return buf;
	}

	// Condition code: JR cc,d encodes it in bits 4..3 (NZ,Z,NC,C); JP/CALL/RET cc use bits 5..3.
	int cc;
	if (op == 0x20 || op == 0x28 || op == 0x30 || op == 0x38)
		cc = (op >> 3) & 3;
	else
		cc = (op >> 3) & 7;

	static const char* kFlag[8] = { "Z80_ZF", "Z80_ZF", "Z80_CF", "Z80_CF",
	                                "Z80_PF", "Z80_PF", "Z80_SF", "Z80_SF" };
	const bool bFlagSet = (cc & 1) != 0;	// odd cc tests "flag set", even tests "flag clear"
	snprintf(buf, sizeof(buf), "%s(%sF & %s)", bFlagSet ? "" : "!", acc, kFlag[cc]);
	return buf;
}

void FCppExporter::EmitTerminator(const FBasicBlock& block)
{
	auto findSuccessor = [&](EEdgeType type) -> const FFlowEdge*
	{
		for (const FFlowEdge& e : block.Successors)
			if (e.Type == type)
				return &e;
		return nullptr;
	};

	auto emitGotoOrJump = [&](FAddressRef target)
	{
		if (CFG.GetBlockStartingAt(target) != nullptr)
			Output("\tgoto %s;\n", BlockLabel(target).c_str());	// stays inside this TU
		else
			Output("\tJumpTo(cpu, 0x%04X); return;   // target outside translated range\n", target.Address);
	};

	switch (block.Terminator)
	{
		case EBlockTerminator::FallThrough:
		{
			const FFlowEdge* pEdge = findSuccessor(EEdgeType::FallThrough);
			if (pEdge != nullptr)
				emitGotoOrJump(pEdge->To);
			else
				Output("\treturn;   // falls off translated range\n");	// keep the label statement-terminated
			break;
		}
		case EBlockTerminator::UncondBranch:
		{
			const FFlowEdge* pEdge = findSuccessor(EEdgeType::Jump);
			if (pEdge != nullptr)
				emitGotoOrJump(pEdge->To);
			else
				Output("\tDispatchIndirect(cpu, %sPC); return;   // unresolved jump\n", Acc());
			break;
		}
		case EBlockTerminator::CondBranch:
		{
			const FFlowEdge* pBranch = findSuccessor(EEdgeType::Branch);
			const FFlowEdge* pFall   = findSuccessor(EEdgeType::FallThrough);
			if (pBranch != nullptr)
				Output("\tif (%s) goto %s;\n",
					ConditionExpr(block.LastInstruction).c_str(), BlockLabel(pBranch->To).c_str());
			if (pFall != nullptr)
				emitGotoOrJump(pFall->To);
			break;
		}
		case EBlockTerminator::Return:
		{
			// A fall-through successor exists iff this was a *conditional* RET.
			const FFlowEdge* pFall = findSuccessor(EEdgeType::FallThrough);
			if (pFall != nullptr)
			{
				Output("\tif (%s) return;\n", ConditionExpr(block.LastInstruction).c_str());
				emitGotoOrJump(pFall->To);
			}
			else
			{
				Output("\treturn;\n");
			}
			break;
		}
		case EBlockTerminator::IndirectJump:
			// TODO(Phase 0 pass 4 / Phase 1): pass the real index register (HL/IX/IY).
			Output("\tDispatchIndirect(cpu, /* TODO: index reg */ %sPC); return;\n", Acc());
			break;
		case EBlockTerminator::Halt:
			Output("\t%sHalted = true; return;\n", Acc());
			break;
		default:
			break;
	}
}

void FCppExporter::EmitBasicBlock(const FBasicBlock& block)
{
	FCodeAnalysisState& state = pEmulator->GetCodeAnalysis();

	Output("%s:   // 0x%04X .. 0x%04X%s%s\n",
		BlockLabel(block.StartAddress).c_str(),
		block.StartAddress.Address, block.EndAddress.Address,
		block.bIsEntry ? "  [entry]" : "",
		block.bContainsSelfModifyingCode ? "  [SMC]" : "");

	if (block.bContainsSelfModifyingCode)
		Output("\t// NOTE: self-modifying code in this block - route through fallback interpreter (Phase 1).\n");

	// Emit each instruction in the block in address order.
	FAddressRef addr = block.StartAddress;
	while (addr.IsValid() && addr <= block.LastInstruction)
	{
		const FCodeInfo* pCodeInfo = state.GetCodeInfoForAddress(addr);
		const uint16_t byteSize = (pCodeInfo != nullptr && pCodeInfo->ByteSize > 0) ? pCodeInfo->ByteSize : 1;

		EmitInstruction(addr);

		if (state.AdvanceAddressRef(addr, byteSize) == false)
			break;
	}

	EmitTerminator(block);
	Output("\n");
}

// -------------------------------------------------------------------------------------
// Top-level program emission
// -------------------------------------------------------------------------------------

bool FCppExporter::ExportProgram(uint16_t startAddr, uint16_t endAddr)
{
	FCodeAnalysisState& state = pEmulator->GetCodeAnalysis();

	const FAddressRef start = state.AddressRefFromPhysicalAddress(startAddr);
	const FAddressRef end   = state.AddressRefFromPhysicalAddress(endAddr);

	if (BuildCFGForAddressRange(state, start, end, CFG) == false)
		return false;

	// Entry-point forward declarations now that the CFG exists.
	SetOutputToHeader();
	EmitEntryPointDeclarations();

	SetOutputToBody();
	Output("// ---- Translated code ---------------------------------------------------\n");

	// SKELETON: a single translation function with all blocks as goto labels. A later
	// milestone splits this per entry point (each entry becomes its own function whose
	// body is `goto L_<entry>;` into the shared label space, or its own block subset).
	Output("void Program_%04X(%s)\n{\n", startAddr, CpuArgDecl());

	for (const auto& blockPair : CFG.Blocks)	// std::map => address order
		EmitBasicBlock(blockPair.second);

	Output("}\n");

	// Report what still needs resolving so the user knows the output's limitations.
	if (CFG.UnresolvedEdges.empty() == false)
	{
		Output("\n// %zu unresolved indirect jump(s) - resolve via runtime traces or annotation:\n",
			CFG.UnresolvedEdges.size());
		for (const FFlowEdge& e : CFG.UnresolvedEdges)
			Output("//   from 0x%04X\n", e.From.Address);
	}

	return true;
}

// -------------------------------------------------------------------------------------
// Free entry point (mirrors ExportAssembler)
// -------------------------------------------------------------------------------------

bool ExportCpp(FEmuBase* pEmu, const char* pFilename, uint16_t startAddr, uint16_t endAddr, bool bEmitC)
{
	FCppExporter exporter;
	if (exporter.Init(pFilename, pEmu) == false)
		return false;

	exporter.SetTargetLanguageC(bEmitC);	// must precede AddHeader (it emits language-specific preamble)
	exporter.SetOutputToHeader();
	exporter.AddHeader();

	const bool bSuccess = exporter.ExportProgram(startAddr, endAddr);

	exporter.Finish();
	return bSuccess;
}
